  createCompleteBipartiteGraph(m, n) {
    // create nodes
    const m_nodes = Array.from({ length: m }, (_, i) => ({
      data: {
        id: `m_${i}`,
        type: "m",
        label: `M ${i}`,
        concentric: 100,
        color: "red",
        shape: "rectangle"
      },
      position: { x: 200 + 100 * i, y: 1000 }
    }));

    // create nodes
    const n_nodes = Array.from({ length: n }, (_, i) => ({
      data: {
        id: `n_${i}`,
        type: "n",
        label: `N ${i}`,
        concentric: 2000,
        color: "blue",
        shape: "ellipse"
      },
      position: { x: 200 + 100 * i, y: 200 }
    }));

    const edges = [];
    for (let i = 0; i < m; i++) {
      for (let j = 0; j < n; j++) {
        edges.push({
          data: {
            source: m_nodes[i]["data"].id,
            target: n_nodes[j]["data"].id
          }
        });
      }
    }
    return [...m_nodes, ...n_nodes, ...edges];
  }

