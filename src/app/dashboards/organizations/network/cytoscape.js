  graphQLConnectionToGraph(graphql_json, root_node_key, relationship_key) {
    const rootNode = graphql_json.data[root_node_key];
    const gqlEdges = rootNode[relationship_key]["edges"];
    const nodes = [
      {
        data: {
          id: rootNode.id,
          label: rootNode?.name || "Root Node"
        }
      },
      ...gqlEdges.map(edge => ({
        data: {
          id: edge.node.id,
          label: edge.node.name
        }
      }))
    ];
    const edges = gqlEdges.map(edge => ({
      data: {
        source: rootNode.id,
        target: edge.node.id
      }
    }));
    return [...nodes, ...edges];
  }

  /**
   * Creates a network with the given number of nodes.
   *
   * @param {number} n - The number of nodes in the network.
   * @returns {Array} - An array containing the nodes and edges of the network.
   */
  createNetwork(n) {
    //@assistant: what does this function do?

    // create nodes
    const nodes = Array.from({ length: n }, (_, i) => this.createNode(i + 1));

    // create edges
    const edges = Array.from({ length: n }, (_, i) =>
      Array.from({ length: n - (i + 1) }, (_, j) => this.createEdge(i + 1, i + j + 2))
    ).flat();

    return [...nodes, ...edges];
  }

